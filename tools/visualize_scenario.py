def fix_svg_latex_escapes(svg_path):
    """Post-process SVG to escape underscores in text tags for LaTeX inkscapelatex."""
    with open(svg_path, "r") as f:
        content = f.read()

    def repl(match):
        return match.group(0).replace("_", "\\_")

    new_content = re.sub(r"<text[^>]*>.*?</text>", repl, content, flags=re.DOTALL)
    with open(svg_path, "w") as f:
        f.write(new_content)


