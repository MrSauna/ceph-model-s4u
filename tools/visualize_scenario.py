        if df_rec.empty:
            continue

        # Calculate Backlog
        df_rec["backlog"] = df_rec["backfill"] + df_rec["backfill_wait"]

        # Track max concurrency for offset calculation
        if not df_rec["backfill"].empty:
            max_concurrency = max(max_concurrency, df_rec["backfill"].max())

